    <!-- 政务处理 -->
          <p data-role="original-title" style="display:none">原标题：贵州省遵义市政府原常务副市长王祖彬被“双开”</p>
            <p>新京报快讯 据中央纪委国家监委网站消息，日前，经贵州省委批准，贵州省纪委监委对遵义市委原常委、市政府原常务副市长王祖彬严重违纪违法问题进行了纪律审查和监察调查。</p> 
<p>经查，王祖彬严重违反政治纪律和政治规矩，在领导干部利用茅台酒谋取私利问题专项整治中不如实报告重大事项;对抗组织审查，向组织提供虚假情况、与他人串供;丧失共产主义理想信念，搞迷信活动。违反中央八项规定精神，收受可能影响公正执行公务的礼品;向从事公务的人员赠送明显超出正常礼尚往来的礼品。违反组织纪律，利用职权为其亲属担任职务及调动工作提供帮助。违反廉洁纪律，利用职权倒卖茅台酒、茅台酒批条获利或将批条提供给其亲属使用。违反国家法律法规，构成职务违法。利用职务上的便利，为他人谋取利益，非法收受他人财物，数额特别巨大，涉嫌受贿犯罪。</p> 
<p>王祖彬身为党员领导干部和国家公职人员，严重违反党的纪律和国家法律法规，且在党的十八大后不收敛、不收手、不知止，性质特别严重，影响特别恶劣，应予严肃处理。依据《中国共产党纪律处分条例》《中华人民共和国监察法》等有关规定，经省纪委常委会省监委委务会会议研究并报省委批准，决定给予王祖彬开除党籍处分，终止其中共遵义市第五次代表大会代表资格;由省监委给予其开除公职处分;收缴其违纪违法所得;将其涉嫌犯罪问题移送检察机关依法审查起诉，所涉财物随案移送。</p> 
<p>(贵州省纪委监委)</p> 
<p>编辑 林玮琪</p> 
